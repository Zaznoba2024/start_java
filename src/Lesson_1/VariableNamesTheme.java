package Lesson_1;

public class VariableNamesTheme {
    public static void main(String[] args) {
        System.out.println("\nЗадание на именование переменных\n");

        System.out.println("1. Разные переменные");
        // цифра
        int number = 8;
        System.out.println("Цифра = " + number);

        // сумма чисел
        int sumNumbers = 2 + 1;
        System.out.println("Сумма чисел = " + sumNumbers);

        // произведение цифр
        int multiplicationDigits = 25;
        System.out.println("Произведение чисел = " + multiplicationDigits);

        // максимальное число
        int max = Integer.MAX_VALUE;
        System.out.println("Максимальное число = " + max);

        // количество десятков
        int numberTens = 15;
        System.out.println("Количество десятков = " + numberTens);

        // вес собаки
        double dogWeight = 7.26;
        System.out.println("Вес собаки = " + dogWeight);

        // исходное число
        double originalDigit = 2.55843;
        System.out.println("Исходное число = " + originalDigit);

        // процент по вкладу
        double rate = 16.29;
        System.out.println("Процент по вкладу = " + rate);

        // переменная хранит символ &
        char symbol = '&';
        System.out.println("Переменная хранит символ & - " + symbol);

        // код ошибки
        String errorCode = "404";
        System.out.println("Код ошибки - " + errorCode);

        // тип сообщения
        String messageType = "Начало диалога";
        System.out.println("Тип сообщения - " + messageType);

        // число нулей
        int numberZeros = 12;
        System.out.println("Число нулей = " + numberZeros);

        // уникальное число
        int uniqueNumber = 1245;
        System.out.println("Уникальное число = " + uniqueNumber);

        // случайное число
        int random = 135;
        System.out.println("Случайное число = " + random);

        // математическое выражение
        String mathExpression = "(7 + 5) * 12";
        System.out.println("Математическое выражение: " + mathExpression);

        // счет в игре
        int score = 10253;
        System.out.println("Счет в игре - " + score);

        // максимальная длина
        long maxLength = Long.MAX_VALUE;
        System.out.println("Максимальная длина = " + maxLength);

        // пункт меню
        String menuItem = "Сохранить как...";
        System.out.println("Пункт меню - " + menuItem);

        // стоимость кофе
        double priceCoffe = 235;
        System.out.println("Стоимость кофе = " + priceCoffe);

        // дата начала
        String beginDate = "13 июля 2024";
        System.out.println("Дата начала - " + beginDate);

        // окончание диапазона
        int endRange = 1000;
        System.out.println("Окончание диапазона = " + endRange);

        // полное имя работника месяца
        String employeeMonthFullName = "Улучшаев Эдуард Семёнович";
        System.out.println("Полное имя работника месяца: " + employeeMonthFullName);

        // заголовок электронной книги
        String ebookTitle = "Всегда говори ДА";
        System.out.println("Заголовок электронной книги: " + ebookTitle);

        // размер
        int size = 42;
        System.out.println("Размер: " + size);

        // вместимость
        int capacity = 10;
        System.out.println("Вместимость - " + capacity);

        // счетчик
        int count = 0;
        System.out.println("Счетчик: " + count);

        // путь до файла
        String filePath = "C:\\Прогноз\\Прогноз_23-25";
        System.out.println("Путь до файла - " + filePath);

        // количество чисел в каждой строке
        int numberDigitsInLine = 12;
        System.out.println("Количество чисел в каждой строке = " + numberDigitsInLine);

        System.out.println("\n2.Boolean-переменные");

        // сотни равны?
        boolean isEqualHundreds = true;
        System.out.println("Сотни равны? " + isEqualHundreds);

        // компьютер включен?
        boolean isComputerOn = false;
        System.out.println("Компьютер включен? " + isComputerOn);

        // есть равные цифры?
        boolean hasEqualDigits = true;
        System.out.println("Есть равные цифры? " + hasEqualDigits);

        // создано?
        boolean isCreated = true;
        System.out.println("Создано? " + isCreated);

        // пустое?
        boolean isEmpty = false;
        System.out.println("Пустое? " + isEmpty);

        // активное?
        boolean isActive = true;
        System.out.println("Активное? " + isActive);

        // новое?
        boolean isNew = false;
        System.out.println("Новое? " + isNew);

        // электронная почта действительная?
        boolean isValidEmail = true;
        System.out.println("Электронная почтв действительная? " + isValidEmail);

        // имеются уникальные строки?
        boolean hasUniqueLine = true;
        System.out.println("Имеются уникальные строки? " + hasUniqueLine);

        System.out.println("\n3.Аббервиатуры");

        // старый universally unique identifier
        String oldUuid = "K-554684/FH";
        System.out.println("старый universally unique identifier " + oldUuid);

        // производитель оперативной памяти
        String ramProducer = "AMD";
        System.out.println("производитель оперативной памяти " + ramProducer);

        // емкость жесткого диска
        int hddCapacity = 500;
        System.out.println("Емкость жесткого диска " + hddCapacity + " ГБ");

        // протокол передачи гипертекста
        String http = "HTTP 1.1";
        System.out.println("Протокол передачи гипертекста " + http);

        // сокращенный uniform resource locator
        String shortUrl = "https://clck.ru/3BvQqq";
        System.out.println("сокращенный uniform resource locator " + shortUrl);

        // новый идентификатор клиента
        String newCuid = "0012345";
        System.out.println("Новый идентификатор клиента " + newCuid);

        // american standard code for information interchange
        int ascii = 12;
        System.out.println("american standard code for information interchange " + ascii);
    }
}
